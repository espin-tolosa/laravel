     * Shows all events since two months ago, time server
     *
     * @return \Illuminate\Http\Response
     */
    public function index()
    {
        $date = Carbon::now()->subMonths(2);
        $events = Event::all()->where('end','>=', $date);
        return EventResource::collection($events);
    }
 
